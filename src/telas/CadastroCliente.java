    private void btnCadastroClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastroClienteActionPerformed
         try {
        String nome = txtNomeCliente.getText();
        String email = txtEmail.getText();
        String telefone = txtTelefone.getText();

        if (nome.isEmpty() || email.isEmpty() || telefone.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos!", "Aviso", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (!email.contains("@") || !email.contains(".")) {
                JOptionPane.showMessageDialog(this, "Digite um email v치lido!", "Aviso", JOptionPane.WARNING_MESSAGE);
                return;
            }

        Usuario novo = new Usuario();
        
        novo.setNome(nome);
        novo.setEmail(email);
        novo.setTelefone(telefone);
        novo.setPendencia(false);

        boolean sucesso = usuarioDAO.cadastrarUsuario(novo);

            if (sucesso) {
                JOptionPane.showMessageDialog(this, 
                    "Usu치rio cadastrado com sucesso!\nID: " + novo.getId(), 
                    "Sucesso", 
                    JOptionPane.INFORMATION_MESSAGE);
                
                
               limparCampos();
                
              
           
                
            } else {
                JOptionPane.showMessageDialog(this, 
                    "Erro ao cadastrar usu치rio no banco de dados!", 
                    "Erro", 
                    JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, 
                "Erro ao salvar usu치rio: " + e.getMessage(), 
                "Erro", 
                JOptionPane.ERROR_MESSAGE);
            e.printStackTrace(); // Para debug
        }
    }//GEN-LAST:event_btnCadastroClienteActionPerformed

